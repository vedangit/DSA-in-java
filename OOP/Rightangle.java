package OOP;
public class Rightangle {
    public static void main(String[] args) {
        int p, q;
        System.out.println("right angle of *");
        for(p = 1; p<=5; p++)
        {
            for(q = 1; q<=p; q++)
            {
                System.out.print("*  ");
            }
            System.out.println(" ");
        }
    }
    
}
